/*****************************************************************  CREACION DE BASE DE DATOS  *****************************************************************/
CREATE DATABASE DBPERUTEC
GO 

USE DBPERUTEC
GO

CREATE TABLE PRODUCTO(
	codProducto int identity(1,1) primary key not null,
	nombre varchar(80) not null,
	descripcion text null,
	codCategoria int foreign key references CATEGORIA(codCategoria) not null,
	stock int not null,
	precio money not null,
	imgReferencial varbinary(max) null
)
go

CREATE TABLE CATEGORIA(
	codCategoria int identity(1,1) primary key not null,
	nomCategoria varchar(80) not null
)
go

CREATE TABLE CLIENTE(
	codCliente int identity(1,1) primary key not null,
	codUbigeo varchar(20) foreign key references UBIGEO(codDistrito) not null,
	nombre varchar(80) not null,
	apellido varchar(80) not null,
	correo varchar(80) not null,
	clave varchar(80)not null,
	dni char(8) not null,
	celular char(9) not null,
	fecNacimiento date not null,
	direccion varchar(80) not null
)
go

CREATE TABLE UBIGEO(
	codDistrito varchar(20) primary key not null,
	nomDistrito varchar(50) not null,
	codProvincia varchar(20) not null,
	nomProvincia varchar(50) not null,
	codDepartamento varchar(20) not null,
	nomDepartamento varchar(50) not null,
)
go


CREATE TABLE PEDIDO(
	codPedido int primary key identity(1,1) not null,
	codCliente int foreign key references CLIENTE(codCliente) not null,
	codMetodoPago int foreign key references METODOPAGO(codMetodoPago) not null, 
	codEstado int foreign key references ESTADO(codEstado) not null,
	fecPedido date not null,
	montoTotal money,
	tiempoEntrega int not null,
	fecPago date not null,
	fecEntrega date not null,
)
go

CREATE TABLE ESTADO(
	codEstado int primary key identity(1,1) not null,
	nomEstado varchar(40) not null,
)
go

CREATE TABLE METODOPAGO(
	codMetodoPago int primary key identity(1,1) not null,
	nomMetodoPago varchar(80) not null
)
go

CREATE TABLE DETALLEPEDIDO(
	codDetallePedido int primary key identity(1,1) not null,
	codProducto int foreign key references PRODUCTO(codProducto) not null,
	codPedido int foreign key references PEDIDO(codPedido) not null,
	cantidadProducto int not null,
	subtotal money not null
)
go


/***********************************************/
CREATE TABLE CARGO(
    codCargo int primary key identity(1,1) not null,
    nomCargo varchar(80) not null
)
GO

CREATE TABLE EMPLEADO(
    codEmpleado int identity(1,1) primary key not null,
    codCargo int foreign key references CARGO(codCargo) not null,
    nombre varchar(80) not null,
    apellido varchar(80) not null,
    correo varchar(80) not null,
    clave varchar(80)not null,
    dni char(8) not null,
    celular char(9) not null,
    fecNacimiento date not null,
)
GO


CREATE TABLE CARRITOS(
codCarritos int primary key identity(1,1) not null,
codCliente int foreign key references Cliente(codCliente) not null,
fechaGuardado date,
montoTotal money,
estado bit not null
)
GO

CREATE TABLE DETALLECARRITOS(
codDetalleCarrito int primary key identity(1,1) not null,
codCarritos int foreign key references CARRITOS(codCarritos) not null,
codProducto int foreign key references Producto(codProducto) not null,
cantidadProducto int not null,
subtotal money not null
)
GO

